--solving logistic equation u'(t) = u(t)(1-u(t)) by euler single step
--initial value u(0.0)=0.5

--defining the function
function derivative (t, ut)
    
    return ut*(1-ut)

end

--initial values
t0 = 0.0
u0 = 0.5
tn = 3.0
n = 30

--euler step h=0.1
h = (tn - t0)/n

--Lua empty tables
t = {}
euler = {}

--populating Lua tables
for i = 1, n+1 do --euler single step
    
    t[#t+1] = t0; euler[#euler+1] = u0
    t0 = t0 + h; u0 = euler[i] + h*derivative(t[i], euler[i])

end

--round operator for decimal figures
function round(num, fig) --decimal figures
    
    return string.format ("%."..fig.."f", num)

end

--displaying results: t, euler, exact ode solution, error
for j = 1, #euler do 
    
    print (t[j], round(euler[j], 14),
    round(math.exp(t[j])/(1+math.exp(t[j])),14),
    round(euler[j] - math.exp(t[j])/(1+math.exp(t[j])),14))

end

